<h2>Bonsai Common Terms</h2>
<div class='group'>
  <p><strong>Apex</strong> - The top or highest section of the tree.</p>
  <p><strong>Back-Budding</strong> - The result of buds growing on the trunk or branch due to pruning.</p>
  <p><strong>Buttress</strong> - The base area of the tree also known as root flare.</p>
  <p><strong>Cambium</strong> - A cellular plant tissue from which phloem, xylem, or cork grows by division, resulting in secondary thickening.</p>
  <p><strong>Callus</strong> - Cells that cover a plant wound after being cut.</p>
  <p><strong>Candle</strong> - The new growth at the tips of branches that typically points upward and resembles a candle sitting on the branch.</p>
  <p><strong>Chop</strong> - Trunk chop is the practice of cutting back a tree aggressively after a healthy branch to develop taper giving the tree a mature appearance in upcoming years.</p>
  <p><strong>Deadwood</strong> - Wood that has died on a tree and can be carved and sealed to give trees a dramatic mature appearance.</p>
  <p><strong>Deciduous</strong> - A type of tree or shrub that sheds its leaves annually in the fall and grows new leaves in the spring.</p>
  <p><strong>Die Back</strong> - Wood dying due to cutting from rot or disease.</p>
  <p><strong>Evergreen</strong> - Having green leaves or needles all year. Evergreen trees lose their leaves individually on an ongoing basis.</p>
  <p><strong>Internode</strong> - The sections of stem between nodes.</p>
  <p><strong>Jin</strong> - Deadwood on a branch that can be carved and preserved and gives the impression of stress exposure from nature.</p>
  <p><strong>Layer</strong> - Air layering, ground layering is the technique to propagate and clone trees.</p>
  <p><strong>Nebari</strong> - The base portion of the tree that consists of the root flare.</p>
  <p><strong>Node</strong> - Nodes are the points on a stem where the buds, leaves, and branching twigs originate.</p>
  <p><strong>Ramification</strong> - The exponential division growth of a plant. 1 branch forks out to 2 branches, then to 4 branches, then to 8 branches and so forth.</p>
  <p><strong>Soil</strong> - The medium a plant grows in. In terms of bonsai soil it's typically granular to allow for fast drainage. There are many types of mixes including the following materials: lava rock, akadama, pine bark, moss, stone, turface and many more material types.</p>
  <p><strong>Sphagnum Moss</strong> - </p>
  <p><strong>Style</strong> - The manipulation of a tree using wires, pruning and other techniques to formulate the tree's design.</p>
  <p><strong>Xylem</strong> - The vascular tissue in plants that conducts water and dissolved nutrients upward from the root and also helps to form the woody element in the stem.</p>
  <p><strong>Shari</strong> - Deadwood on trunk that can be carved and preserved and gives the impression of stress exposure from nature.</p>
  <p><strong>Yamadori</strong> - A collected mature tree from nature.</p>
