    <strong>Air Layering</strong> -
    This is one of the best techniques to get mature specimens as long as you have access to trees. I happen to have a lot of trees around me so I always have several air layers going. Don't underestimate air layering. You can buy small trees but it takes many years for them to mature. You can air layer large branches and within a few months the roots are strong enough to sustain a new tree. This shaves off years and costs far less than buying large trees! To learn more about this technique see my <a href='/how-to-air-layer-trees'>How To Air Layer Trees</a> article.
  </li>

  <li>
    <strong>Nursery Stock</strong> -
    This is how I started. I bought a <em>Blue Point Juniper</em> from Home Depot and it's my first official bonsai tree. There are several varieties available at nurseries and depending on your area you can find some really amazing specimens. Some more expensive than others. Starting small is best as a beginner. 1, 3, 5 gallon type plants are excellent. Some great start plants could be <em>Blue Point Juniper, Blue Rug Juniper, Ficus, Boxwood</em> are all excellent starters and common in most zones. As you develop and gain experience you can really find some amazing specimens out there.
  </li>

  <li>
    <strong>Digging Up a Tree</strong> -
    Collecting trees in the wild is a great technique as well as you can get great mature specimens. Digging is hard work and timing is key. Do your research and get to digging. Many local clubs have season excursions where they go onto pre-approved land and dig up trees.
  </li>

  <li>
