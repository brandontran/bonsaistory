<p>
Air layering is one of my favorite techniques to propagate a tree as it shaves off years of trying to grow from seed. It also shaves money off of the price of a new tree because it's basically free. Buying mature trees is expensive but if you have access to large branches of surrounding trees, air layer them and get more trees! Don't underestimate air layering. You can buy small trees but it takes many years for them to mature.
</p>

<h3>Items Needed</h3>
  <ol>
    <li>Sharp Knife</li>
    <li>Root Hormone - $5 at Home Depot</li>
    <li>Sphagnum Moss - $5 at Home Depot</li>
    <li>Tin Foil, Plastic Wrap</li>
    <li>Bucket of Water</li>
    <li>Tape</li>
  </ol>

<h3>Steps</h3>
<ol>
  <li>Start In Spring</li>
  <li>Cut a ring around the branch or trunk</li>
  <li>Apply root hormone to the top part of the cut ring</li>
  <li>Soak moss in water and squeeze it out</li>
  <li>Wrap the moss around the tree and with your wrap tightly</li>
  <li>Tape top, bottom and middle of the bundle</li>
  <li>Lightly fertilize the tree</li>
  <li>Wait 2-4 months while checking roots periodically</li>
  <li>Cut below the air layer section and plant tree</li>
</ol>
